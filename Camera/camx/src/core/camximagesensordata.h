    /// GetColorFilterPattern
    ///
    /// @brief  Get color filter information
    ///
    /// @param  resolutionIndex Current resolution index
    ///
    /// @return Color filter pattern
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
