    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    static ImageSensorlib* OpenSensorLibrary(
        const CHAR* pName);

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// GetInitSettingIndex
    ///
    /// @brief  Get the init setting index based on sensor version
    ///
    /// @param  sensorVersion Sensor version
    ///
