    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    UINT32 GetInitSettingIndex(
        UINT32 sensorVersion);

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// GetResSettingIndex
    ///
    /// @brief  Get the resolution setting index based on sensor version
    ///
    /// @param  sensorVersion Sensor version
