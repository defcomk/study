    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    VOID GetNoiseCoeff(
        NoiseCoefficent*        pNoiseCoeff,
        NoiseCoefficientChannel channel
    ) const;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// GetNoiseModelEntryS
    ///
    /// @brief  Maps the sensitivity to the S noise parameter in the DNG noise model
