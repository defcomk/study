    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// GetDigitalRegGain
    ///
    /// @brief  Get register data for sensor digital gain
    ///
    /// @param  pExposureInfoParam  Pointer to exposure info
    ///
