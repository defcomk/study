    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    VOID FillExposureArray2ByteIntegrationTime(
        UINT32  analogGain,
        UINT32  digitalGain,
        UINT32  lineCount,
        UINT32  shortRegisterGain,
        UINT32  shortLinecount,
        UINT32  frameLengthLine,
        VOID*   pSetting,
        BOOL    applyShortExposure);

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// TranslateColorFilterArrangement
